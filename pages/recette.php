    <h1 class="point2">Gratin de blette</h1>
    Tag : Plat<br>
    Un bon gratin qui rend honneur aux blettes<br>
    <img src="PhotoRecette/pic01.jpg" width="300px" class="imgRecette">
    <h3 class="point3">Liste ingrédients : </h3>
    Épinards, deux belles poignées<br>
    Ricotta, un pot<br>
    Parmesan, 60g + 30g<br>
    lait 1/2 litre + 5 à 10cl<br>
    beurre 23g<br>
    Farine 20g<br>
    Des lasagnes sèches ou fraîches<br>
    Sel poivre, muscade<br>
</main>

<aside class="sidebar-Droit" style="width:50%">
    <div class="boxDroit">
        <h4>ÉTAPE 1</h4>
        On prend des épinards, on les lave, on les émince grossièrement et on réserve.
        <h4>ÉTAPE 2</h4>
        On prend un saladier, on met la riccota, le parmesan et un peu de lait (5 à 10 cl)
        On touille et on réserve.
        <h4>ÉTAPE 3</h4>
        La béchamel :<br>
        Le beurre va dans une casserole, feu doux, jusqu'à ce qu'il soit fondu.
        Quand il est fondu, on prend un peu de beurre qu'on met dans le plat à lasagne pour le
        graisser un peu (3 grammes environ)<br>
        On ajoute la farine et on touille avec un fouet. ça devrait faire rapidement une boule
        jaune.<br>
        On ajoute un petit peu de lait, on touille et ainsi de suite jusqu'à ce que la farine et le
        beurre soit complètement incorporé et intégré au lait.
        <h4>ÉTAPE 4</h4>
        Et là, deux écoles, les impatients et les patients.<br>
        Les patients laissent à feu doux et touillent pendant bien 20 minutes.<br>
        Les impatiens augmentent progressivement le feu et vont se faire une bière, en règle
        générale, la béchamel est foiré.
        <h4>ÉTAPE 5</h4>
        Une fois que la béchamel est prète (cad, qu'elle s'est épaissie suffisamment pour que le
        fouet laisse des marques visible dans la mixture lorsque l'on touille), on ajoute un peu de
        muscade, on sale (un peu, attention, il y a le parmesan qui sale déjà) et on poivre.
        <h4>ÉTAPE 6</h4>
        On préchauffe le four à 180°c


