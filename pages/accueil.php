    <h1 class="point1" id="acc1">Pourquoi ?</h1>
    Parce qu’on monte notre AMAP comme un groupe punk, avec pas grand chose, de
    l’huile de coude, depuis le garage d’un.e pote.<br><br>
    Parce qu’on revendique une solidarité et une entraide à l’instar de la culture punk.<br><br>
    Parce que le mouvement punk a été monté chargé d’insolence et de défiance face à
    un système dogmatique et violent. On croit que la consommation est dogmatique, qu’elle
    alimente une part de violence dans la société ; on est content de lui faire un pied de nez,
    la crête au vent ; à cette consommation.<br><br>
    Parce qu’on aime savoir ce qu’il y a dans notre assiette, qu’on veut soutenir celui ou
    celle qui bosse pour nous donner de bonnes choses à manger et qu’on s’y engage, même
    en cas de coup dur.<br><br>
    Au delà de bon produits, payés à un prix que l’on considère juste ; on est heureux de
    se réunir régulièrement pour partager un moment simples dans un esprit de partage, de
    bonne humeur et de convivialité.<br><br>
    Parce que tout seul, on va pas loin, on s’engage à participer à un collectif pour y faire
    vivre cette recherche de sens.<br><br>
    Pour finir, parce qu’on trouve que ce nom est absurde, qu’il nous fait marrer et ça, y’a
    rien de plus punk !<br><br>
    <br>
