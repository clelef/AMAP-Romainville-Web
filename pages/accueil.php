    <h1 class="point1" id="acc2">Qu’est-ce que c'est ?</h1>
    Une AMAP (Association pour le Maintien d’une Agriculture Paysanne) est un partenariat
    entre un groupe de gens, comme toi et moi et un.e paysan.ne.<br>
    Ce partenariat prend la forme d’un engagement réciproque qui permet la rencontre,
    chaque semaine entre le groupe de citoyen.ne.s et l.e.a paysan.ne.<br>
    Ce.lle.lui-ci apporte sa récolte financée au préalable par les membres du collectif que
    l’on appelle habituellement des «amapien.ne.s» et qui chez nous se nomment les «pépins».<br>
    Une AMAP est donc un système de vente directe, sans intermédiaires entre un.e
    product.rice.eur et des consommat.rices.eurs.<br>
    On ne va pas t’en dire plus, tu peux trouver plus de détails en téléchargeant la charte
    des AMAP à l’adresse suivante : https://frama.link/charteamap<br>
